#include <iostream>
using namespace std;

int main() {
	cout << "Hello world!" << endl;
	int x; // Initialises x
	cout << "Please enter a number: "; // Enter a number and hit RETURN
	cin >> x; // Input value
	cout << "Your number is: " << x << endl; // Prints the input
	cout << "Have a nice day.";
	cout << endl;
	return 0;
}
