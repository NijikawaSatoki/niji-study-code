#include <iostream>
using namespace std;

int main() {
	int myNum = 15; // Variable "myNum" is 15
	cout << "My number is " << myNum << "." << endl; // Outputs "15"
	myNum = 10; // Variable "myNum" is now 10
	cout << "My new number is " << myNum << "."; // Outputs "10"
	cout << endl;
	return 0;
}
