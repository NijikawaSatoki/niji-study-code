// Declaring Multiple Variables (Example 2)

#include <iostream>
using namespace std;

int main() {
	cout << "Hello world!" << endl;
	int x, y, z;
	x = y = z = 50;
	cout << "Your total comes out to " << x + y + z << ".";
	cout << endl;
	return 0;
}
