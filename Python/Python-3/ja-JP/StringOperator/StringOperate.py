# 一部の演算子は文字列で使用できます。注目すべきは「+」です。
hello = "こんばんは、"		# 変数「hello」は「こんばんは、」にセットします
world = "世界！"		# 変数「world」は「世界！」にセットします
helloworld = hello + world	# 変数「hello」と変数「world」が連結されます
print(helloworld)		# 「helloworld」の価を出力します
