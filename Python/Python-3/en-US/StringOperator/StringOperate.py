# Some operators can be used on strings. Of note is '+'
hello = "Hello,"			# Variable 'hello' is set to 'Hello,'
world = "world!"			# Variable 'world' is set to 'world!'
helloworld = hello + " " + world	# Variables 'hello', 'world', and string ' ' are concatenated
print(helloworld)			# Prints the value of 'helloworld'
